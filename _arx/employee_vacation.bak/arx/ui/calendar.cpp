#include "calendar.h"

namespace rele_auto
{

namespace employee_vacation
{
//#############################################################################
//			class calendar
//#############################################################################

    //=========================================================================
    //		CONSTRUCTORS/DESTRUCTOR
    //=========================================================================

    ///-------------------------------------------------------------------------
    ///	calendar( )
    ///
    calendar::calendar( QWidget *parent /*= 0*/ ) :
        QCalendarWidget( parent )
    {
        this->initialize( );
    }

    ///------------------------------------------------------------------------
    ///	~calendar( )
    ///
    calendar::~calendar( )
    {
    }

    //=========================================================================
    //		PROPERTIES
    //=========================================================================

    //=========================================================================
    //		OPERATORS
    //=========================================================================

    //=========================================================================
    //		FUNCTIONS
    //=========================================================================
    void calendar::initialize( )
    {
        this->setFirstDayOfWeek( Qt::Monday );
        this->setSelectionMode( QCalendarWidget::NoSelection );
    }

//#############################################################################

}// namespace employee_vacation

}//namespace rele_auto
